package com.cdhxqh.travel_ticket_app.weather;

public class ChartPoint {
	private double x;
	private double y;
	
	public ChartPoint() {
		super();
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public ChartPoint(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	
}
